@bot.message_handler(func=lambda m: m.text in ["🇷🇺 Русский", "🇬🇧 English"])
def set_language(m):
    uid = m.from_user.id
    if m.text == "🇷🇺 Русский":
        user_language[uid] = "ru"
    else:
        user_language[uid] = "en"
    bot.send_message(m.chat.id, get_text(uid, "lang_changed"), parse_mode="Markdown", reply_markup=get_start_keyboard())

@bot.message_handler(func=lambda m: m.text == "▶️ START")
def handle_start(m):
    uid = m.from_user.id
    bot.send_message(m.chat.id, get_text(uid, "enter_password"), parse_mode="Markdown", reply_markup=remove_kb())
    bot.register_next_step_handler(m, check_pass)

def check_pass(m):
    uid = m.from_user.id
    if m.text.strip() == DEMO_PASSWORD:
        authorized_users.add(uid)
        bot.send_message(m.chat.id, get_text(uid, "access_granted"), parse_mode="Markdown", reply_markup=get_main_keyboard())
    else:
        bot.send_message(m.chat.id, get_text(uid, "wrong_password"), parse_mode="Markdown", reply_markup=get_start_keyboard())

@bot.message_handler(func=lambda m: m.text == "🔎 Проверить текст")
def ask_text(m):
    uid = m.from_user.id
    if uid not in authorized_users:
        bot.send_message(m.chat.id, get_text(uid, "wrong_password"), reply_markup=get_start_keyboard())
        return
    bot.send_message(m.chat.id, get_text(uid, "ask_for_text"), parse_mode="Markdown", reply_markup=remove_kb())
    bot.register_next_step_handler(m, analyze)

def analyze(m):
    uid = m.from_user.id
    text = m.text.strip()
    if not text:
        bot.send_message(m.chat.id, get_text(uid, "empty_text"), reply_markup=get_main_keyboard())
        return
    
    bot.send_chat_action(m.chat.id, "typing")
    lang = detect_lang(text)
    
    if lang == "unknown":
        bot.send_message(m.chat.id, get_text(uid, "unknown_lang"), reply_markup=get_main_keyboard())
        return
    
    lang_name = "русский" if lang == "russian" else "English"
    bot.send_message(m.chat.id, get_text(uid, "analyzing").format(lang_name))
    
    if lang == "russian":
        res, err = check_russian(text)
    else:
        res, err = check_english(text)
    
    answer = format_result(uid, lang, res, err)
    bot.send_message(m.chat.id, answer, parse_mode="Markdown", reply_markup=get_main_keyboard())

