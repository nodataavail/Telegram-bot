def get_text(user_id, key):
    lang = user_language.get(user_id, "ru")
    return TEXTS.get(lang, TEXTS["ru"]).get(key, TEXTS["ru"][key])

def get_start_keyboard():
    kb = ReplyKeyboardMarkup(resize_keyboard=True, row_width=2)
