def remove_kb():
    return telebot.types.ReplyKeyboardRemove()

# ========== ОСНОВНЫЕ ФУНКЦИИ ==========
def check_russian(text):
    try:
        prompt = f"Ты детектор ИИ. Ответь JSON: {{'is_ai': true/false, 'confidence': 0-100, 'explanation': 'объяснение'}}. Текст: {text[:1000]}"
        resp = giga.chat(prompt)
        reply = resp.choices[0].message.content
        match = re.search(r'\{.*\}', reply, re.DOTALL)
        if match:
            res = json.loads(match.group())
            return {"is_ai": res.get("is_ai", False), "confidence": res.get("confidence", 50), "explanation": res.get("explanation", "")}, None
        return {"is_ai": False, "confidence": 50, "explanation": "Не удалось распознать"}, None
    except Exception as e:
        return None, str(e)

def check_english(text):
    if len(text.strip()) < 20:
        return {"is_ai": False, "confidence": 0, "explanation": None}, None
    try:
        r = requests.post(SAPLING_URL, json={"key": SAPLING_API_KEY, "text": text}, timeout=30)
        if r.status_code == 200:
            score = r.json().get("score", 0.5)
            return {"is_ai": score > 0.6, "confidence": int(score * 100), "explanation": None}, None
        return None, f"API error: {r.status_code}"
    except Exception as e:
        return None, str(e)

def detect_lang(text):
    try:
        lang = detect(text)
        if lang == 'ru': return "russian"
        if lang == 'en': return "english"
        return "unknown"
    except:
        return "unknown"

def format_result(user_id, lang, detection, error):
    if error:
        return get_text(user_id, "error").format(error)
    if not detection:
        return "❌ Ошибка"
    
    is_ai = detection["is_ai"]
    conf = detection["confidence"]
    icon = "🤖" if is_ai else "👤"
    color = "🔴" if is_ai else "🟢"
    status = get_text(user_id, "ai") if is_ai else get_text(user_id, "human")
    lang_display = get_text(user_id, "russian") if lang == "russian" else get_text(user_id, "english")
    
    res = f"{icon} *{get_text(user_id, 'result_title')}*\n\n{color} *{status}*\n\n🌐 *{get_text(user_id, 'lang_label')}:* {lang_display}\n📊 *{get_text(user_id, 'confidence_label')}:* {conf}%"
    
    if lang == "russian" and detection.get("explanation"):
        res += f"\n\n💬 *Объяснение:* {detection['explanation']}"
    return res

